/**************************************************************************
**
** 函数名称：Delay_ms(uint16_t time)
** 函数功能：延时函数，延时时间time毫秒
**
**************************************************************************/

void Delay_ms(uint16_t time)
{
	uint16_t i,j;
	for(i = 0;i < time;i++)
		for(j = 0;j < 10260;j++);
}
